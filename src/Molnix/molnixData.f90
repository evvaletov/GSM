! ======================================================================
!
!   This block data routine enters the microscopic correction to the
!   macroscopic mass of the spherical macroscopic ground state and the
!   experimental mass excess, (or the calculated one for nuclei without
!   a measured mass excess) according to the Finite-Range Liquid Drop
!   Model of nuclear masses. [P. Moller, J. R. Nix, W. D. Myers, and
!   W. J. Swiatecki, Atomic Data Nucl. Data Tables, 59, 185 (1995)]
!   The less accurate FRLDM is used (instead of the FRDM), because that
!   is the model used to calculate fission barrier heights in the Sierk
!   BARFIT routine which is used in CEM03.
!
!   The routine also enters the pairing gap (energy shift) to be used
!   in nuclear level density formulas, derived from
!   the tables of Moller, Nix, & Kratz (1996). For odd-Z, even-N nuclei,
!   the energy shift is delta-n from the M, N, & K table;
!   for odd-N, even-Z, delta-p; and for even-even nuclei, the correction
!   is delta-p + delta-n.
!
!   For each value of Z, the values of N from Nmin(Z) to Nmax(Z)
!   are included in the table.
!   The Z index of the arrays nmin, nmax, emic1, epair1, and emx1
!   is equal to Z - 7, since the tables only contain entries for
!   Z,N > 7. For emic2, emx2, & epair2, the index is Z - 34;
!   For emic3, emx3, & epair3, the index is Z - 47; and
!   for emicr, emxr, & epair4, the index is Z - 70.
!   The N index corresponds to N - Nmin(Z) + 1.
!   The table contains Z values from 8 to 100, and N values from the
!   most neutron-deficient isotope in the original table to
!   approximately 5-10 neutrons above the most neutron-rich isotope
!   which exists in nature (or has a long half life, for elements with no
!   stable isotopes).  This asymmetry acknowledges the fact that the
!   isotopes encountered in the decay of nuclei will start near the
!   line of beta stability, and move to the neutron deficient region,
!   as evaporation and pre-equilbrium decay proceed.
!
!   Tables added for 1 <= Z <= 7, 11/97
!
!   All numbers updated to 1995 mass tables (2000 Nuclear Wallet Cards),
!   AJS, March, 2002; September, 2003.
!   Removed "equivalence" statements, 10/9/03, AJS.
!
!    Written by A. J. Sierk,  LANL  T-2,  April-May, 1996.
!    Modified by AJS, November, 1997.
!    Modified by AJS, September, 2003.
!    Modified by A. J. Sierk, LANL T-16, October, 2003.
!    Edited by AJS, LANL T-2, December, 2011.
!
! ======================================================================

